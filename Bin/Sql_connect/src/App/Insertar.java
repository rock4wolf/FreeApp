    private void BtnInserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnInserMouseClicked
        // TODO add your handling code here:
        try {
            conexion = Conection.conexion();
            pst = conexion.prepareStatement("Insert INTO personal_data (ID_personal,age,sex,freq,estado,allergy,desease) VALUES (?,?,?,?,?,?,?)");
            String txt = Txtid.getText();
            pst.setInt(1, Integer.parseInt(txt));
            pst.setInt(2, Integer.parseInt(Txtedad.getText()));
            pst.setString(3, Txtsex.getText());
            pst.setString(4, Txtfreq.getText());
            pst.setString(5, Txtesta.getText());
            pst.setString(6, Txtale.getText());
            pst.setString(7, Txtenf.getText());
            if (pst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(null, "Registro exitoso!");
                cerrarConexion();
            } else {
                JOptionPane.showMessageDialog(null, "Intente de nuevo");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Algo salio mal: " + e.getMessage());
        }
    }//GEN-LAST:event_BtnInserMouseClicked

