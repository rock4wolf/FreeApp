    private void BtnEnterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnEnterMouseClicked
        // TODO add your handling code here:
        MainMenu MM = new MainMenu();
        if(TxtUser.getText() == null || TxtUser.getText()== null){
            JOptionPane.showMessageDialog( null, "Es necesario llenar todos los datos");
        }else{
            try {
                conexion = Conection.conexion();
                pst = conexion.prepareStatement("SELECT * FROM usuario WHERE Username = ? and Pass = ?");
                String User = TxtUser.getText();
                String Pass = JPass.getText();
                pst.setString(1, User);
                pst.setString(2, Pass);
                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                    cerrarConexion();
                    MM.setVisible(true);
                    this.setVisible(false);
                }else{
                    cerrarConexion();
                    JOptionPane.showMessageDialog( null, "Usuario o contrase√±a incorrectos");
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, "Algo salio mal: " + e.getMessage());
                cerrarConexion();
            }
        }
    }//GEN-LAST:event_BtnEnterMouseClicked

