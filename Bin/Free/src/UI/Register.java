    private void BtnEnterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnEnterMouseClicked
        // TODO add your handling code here:
        PersonalData MM = new PersonalData();
        if(Jpass1.getText() == null || Jpass2.getText() == null || TxtMail.getText() == null || TxtUser.getText() == null){
            //VERIFICA QUE NO ESTEN VACIOS LOS CAMPOS
            JOptionPane.showMessageDialog( null, "Es necesario llenar todos los datos");
        }else{
            if(Jpass1.getText().equals(Jpass2.getText())){
                //VERIFICA QUE LAS CONTRASEÃ‘AS SEAN IGUALES
                try {
                    conexion = Conection.conexion();
                    pst = conexion.prepareStatement("SELECT * FROM usuario WHERE Username = ?");
                    String txt = TxtUser.getText();
                    pst.setString(1, txt); 
                    ResultSet rs = pst.executeQuery();
                    if(rs.next()){
                        //buscar si existe el usuario
                        //el caso que se encuentre es aqui
                        cerrarConexion();
                        JOptionPane.showMessageDialog(null, "nombre de usuario ya existente, ingrese otro");
                        TxtUser.setText(null);
                        TxtUser.requestFocus();
                    }else{
                        try {
                            conexion = Conection.conexion();
                            pst = conexion.prepareStatement("Insert INTO usuario(Mail,Username,Pass) VALUES (?,?,?)");
                            pst.setString(1,TxtMail.getText());
                            pst.setString(2,TxtUser.getText());
                            pst.setString(3, Jpass1.getText());
                            if (pst.executeUpdate() > 0) {
                                cerrarConexion();
                                JOptionPane.showMessageDialog( null, "Usuario creado con exito");
                                Usuario = TxtUser.getText();
                                MM.setVisible(true);
                                this.setVisible(false);
                             } else {
                                JOptionPane.showMessageDialog(null, "Intente de nuevo");
                            }
                            } catch (Exception a) {
                                System.out.println(a.getMessage());
                                JOptionPane.showMessageDialog(null, "Algo salio mal: " + a.getMessage());
                            }
                        
                    }
                } catch (Exception e) {
                    System.out.println(e.getMessage());
                    JOptionPane.showMessageDialog(null, "Algo salio mal: " + e.getMessage());
                    cerrarConexion();
                }
            }  
        }   
    }//GEN-LAST:event_BtnEnterMouseClicked

