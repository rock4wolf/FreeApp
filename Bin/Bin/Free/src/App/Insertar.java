 private void cerrarConexion() {
        try {
            if (rs != null) {
                rs.close();
            }
            if (pst != null) {
                pst.close();
            }
            if (conexion != null) {
                conexion.close();
            }
        } catch (Exception e) {
            System.err.println("Error al cerrar conexiones - DaoUsuario " + e.getMessage());
        }
    }
