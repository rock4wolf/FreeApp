    private void btnguardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnguardarMouseClicked
        // TODO add your handling code here:
        try {
            conexion = Conection.conexion();
            pst = conexion.prepareStatement("SELECT * FROM personal_data WHERE ID_personal = ?");
            String txt = Txtid.getText();
            pst.setInt(1, Integer.parseInt(txt));
            rs = pst.executeQuery();
            if (rs.next()) {
                TxtIDD.setText(rs.getString("ID_personal"));
                Txtedad.setText(rs.getString("age"));
                Txtsex.setText(rs.getString("sex"));
                Txtfreq.setText(rs.getString("freq"));
                Txtesta.setText(rs.getString("estado"));
                Txtale.setText(rs.getString("allergy"));
                Txtenf.setText(rs.getString("desease"));
                JOptionPane.showMessageDialog(null, "Datos encontrasdos exitosamente!");
                cerrarConexion();
            } else {
                JOptionPane.showMessageDialog(null, "No existe ese Id");
                cerrarConexion();
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(null, "Algo salio mal: " + e.getMessage());
            cerrarConexion();
        }
    }//GEN-LAST:event_btnguardarMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int resp = JOptionPane.showConfirmDialog(null, "¿Está seguro?");
        System.out.println(resp);
        if (resp == 0) {
            try {
                conexion = Conection.conexion();
                pst = conexion.prepareStatement("DELETE FROM personal_data WHERE ID_personal=?");
                String txt = Txtid.getText();
                pst.setInt(1, Integer.parseInt(txt));
                if (pst.executeUpdate() > 0) {
                    JOptionPane.showMessageDialog(null, "Persona eliminada correctamente!");
                    cerrarConexion();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al eliminar");
                    cerrarConexion();
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, "Algo salio mal: " + e.getMessage());
                cerrarConexion();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

